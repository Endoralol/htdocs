var RandomizeFreedomTag = {
    /**
     * Configuration.
     */
    config: {},
    /**
     * CSS selectors
     */
    query: '',
    /**
     * jQuery objects
     */
    randomizeButton: null,
    freedomTagInput: null,
    /**
     * Initialize the RandomizeFreedomTag singleton and apply the (optional) config.
     *
     * @constructs
     *
     * @param query CSS selector for the container element
     * @param config Optional configuration
     */
    create: function create() {
        query = typeof query === 'undefined' ? '#generate-random-freedomtag' : query;
        this.randomizeButton = $(query);
        // Check if the element exists
        if (!this.randomizeButton.length) {
            return;
        }
        config = typeof config === 'undefined' ? {} : config;
        // Merge configuration
        this.config = $.extend({}, config);
        this.setup();
    },
    /**
     * Sets up the RandomizeFreedomTag class.
     */
    setup: function setup() {
        this.freedomTagInput = $('#freedomTag');
        this.registerEvents();
    },
    /**
     * Register events for interaction. Prefer .on() handlers as of jQuery 1.7.
     *
     * @see http://api.jquery.com/category/version/1.7/
     */
    registerEvents: function registerEvents() {
        this.randomizeButton.on('click',
            $.proxy(this.updateRamdomizeFreedomTag, this));
    },
    updateRamdomizeFreedomTag: function updateRamdomizeFreedomTag() {
        var locale,
            index;
        locale = this.getLocale();
        index = this.getRandomIndex(locale);
        this.freedomTagInput.val(freedomTags[locale][index]).change();
    },
    /**
     * return locale (default : en-us)
     */
    getLocale: function getLocale() {
        var regionLanguage = this.getRegionLanguage(FreedomTagValidator.accountRegion);
        if (freedomTags[Core.locale] && freedomTags.languageMap[regionLanguage].indexOf(Core.locale) >= 0) {
            return Core.locale;
        }
        return 'en-us';
    },
    /**
     * return region language by account Region
     */
    getRegionLanguage: function getRegionLanguage(region) {
        if (['EU', 'NA', 'SE', 'PTR', 'LA'].indexOf(region) >= 0) {
            return "latin";
        } else if (['RU'].indexOf(region) >= 0) {
            return "cyrillic";
        } else if (['KR'].indexOf(region) >= 0) {
            return "korean";
        } else if (['CN', "TW"].indexOf(region) >= 0) {
            return "chinese";
        } else {
            return "latinbasic";
        }
    },
    /**
     * return random index number
     * @param locale locale
     */
    getRandomIndex: function getRandomIndex(locale) {
        return Math.floor(Math.random() * freedomTags[locale].length);
    }
};
